def calcular():
    peso = float(input_peso.get())
    altura = float(input_altura.get())
    calc = round(peso/altura**2, 2)


    lb_resultado = Label(tela1 , text=calc , font = " Time 30 bold" , anchor = "w")
    lb_resultado.place(width= 120 ,height=40 , x = 210 , y =40 )



    if calc < 18.5:
        grau = " Abaixo do peso"
        pic = Img1
    elif calc < 24.9:
        grau = "Peso normal"
        pic= Img2
    elif calc < 29.9:
        grau = "Acima do peso"
        pic = Img3
    elif calc < 39.9:
        grau = "Obsidade d egrau 1 "
        pic = Img4
    else:
        grau = "Obesidade de grau 2 "
        pic = Img5
    lb_imcgrau = Label(tela1 , text =grau, font=  " Time 12 bold " , anchor= "w")
    lb_imcgrau.place(width = 140 , height= 20 , x = 80 , y= 150 )

    lb_picture = Label(tela1 , image=pic)
    lb_picture.place(width= 100 , height= 170 , x = 350 , y = 50)

    n = round(18.9 * altura * altura, 1)
    f = round(24.9 * altura * altura, 1)

    lb_pesoideal = Label(tela1 , text = f"O seu peso ideal Ã© entre {n} e {f} kg" , font= " Time 10 bold")
    lb_pesoideal.place(width=250 , height= 20, x = 10 , y = 100)

